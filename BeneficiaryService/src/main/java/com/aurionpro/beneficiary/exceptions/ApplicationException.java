package com.aurionpro.beneficiary.exceptions;


public class ApplicationException extends BaseException {

	private static final long serialVersionUID = 2411415024482966975L;

	public ApplicationException() {
		super();
	}

	public ApplicationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ApplicationException(String arg0) {
		super(arg0);
	}

	public ApplicationException(Throwable arg0) {
		super(arg0);
	}
}
